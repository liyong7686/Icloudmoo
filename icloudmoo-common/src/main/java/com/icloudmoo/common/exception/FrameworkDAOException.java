public class FrameworkDAOException extends FrameworkException {

	private static final long serialVersionUID = 1L;

	public FrameworkDAOException() {
		super();
	}

	public FrameworkDAOException(String arg0) {
		super(arg0);
	}

	public FrameworkDAOException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FrameworkDAOException(Throwable arg0) {
		super(arg0);
	}

}

